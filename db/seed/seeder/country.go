func makeCountry(iso, name, iso3 string, numCode, phoneCode int) countrymodule.Country {
	var iso3Val *string
	if iso3 != "" {
		iso3Val = &iso3
	}

	var numCodeVal *int
	if numCode != -1 {
		numCodeVal = &numCode
	}

	return countrymodule.Country{
		Iso:       iso,
		Iso3:      iso3Val,
		Name:      name,
		NumCode:   numCodeVal,
		PhoneCode: phoneCode,
	}
}
